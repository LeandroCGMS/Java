package com.leandro.aula85_100;

import static java.lang.Math.*;

public class Aula85Math {

	public static void main(String[] args) {

		System.out.println(pow(2, 3));

		System.out.println(round(4.7));

		System.out.println(round(4.4));

		System.out.println(ceil(4.7));

		System.out.println(ceil(4.4));
		
		System.out.println(floor(4.7));

		System.out.println(floor(4.4));
		
		System.out.println(round(random() * 100));
		
		System.out.println(sin(toRadians(30)));
		
		System.out.println(cos(toRadians(1)));
		
		System.out.println(tan(toRadians(45)));

	}

}
